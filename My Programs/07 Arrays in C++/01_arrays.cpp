#include <iostream>
using namespace std;

int main(){
    int arr[5]; // declaring an array

    // initialising an array 
    arr[0] = 10;
    arr[1] = 20;
    arr[2] = 30;
    arr[3] = 40;
    arr[4] = 50;

    // accessing an array
    cout << arr[0] << endl;
    cout << arr[1] << endl;
    cout << arr[2] << endl;
    cout << arr[3] << endl;
    cout << arr[4] << endl;   
    return 0;
}